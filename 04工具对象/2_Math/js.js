// 封装了很多属性和方法

console.log(Math.PI);                   // 3.141592653589793
console.log(Math.E);                    // 2.718281828459045

// 绝对值
console.log(Math.abs(-10));             // 10


// 正弦余弦函数
console.log(Math.cos(Math.PI));         // -1
console.log(Math.sin(0));               // 0


// 上舍入
console.log(Math.ceil(15.44));          // 16


// 返回0-1之间的随机数
console.log(Math.random());             // 0.777457622066974


// 生成一个 x 到 y 的随机数
var x = 2,y = 10
console.log(Math.round(Math.random()*(y-x)+x))          // 5


// x 的 y 次方
console.log(Math.pow(x,y))              // 1024


// 开方运算
console.log(Math.sqrt(x));              // 1.4142135623730951


